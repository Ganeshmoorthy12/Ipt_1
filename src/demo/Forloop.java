package demo;

public class Forloop {
	
	
	public static void main(String[] args) {
		
	int i;
		
	for (i = 0; i < 5 ; i++) {
		
		for (float j = 0; j <= 3; j++) {
			
			for (int k = 10; k > 0 ;k--) {
				
				System.out.println(i+""+j+" "+k);
				
			}
			
			System.out.println();
			
		}
		
	}
	
		
		
		
		
	}
	
}
